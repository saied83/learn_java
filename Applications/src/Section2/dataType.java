package Section2;
public class dataType {
    public static void main(String[] args){
        short shortNum = 1;      // 16 bit
        int number = 12;     // 32 bit
        long myNumber = 12324;     // 64 bit

        float floatingNum = 3.4545f; // 32 bit
        double doubleNumber = 2.3434; // 64 bit

        boolean isTrue = true; // boolean

        char letter = 'a';
        String name = "Saiedur Rahman Saied";

        System.out.println(shortNum);
        System.out.println(number);
        System.out.println(myNumber);;
        System.out.println(floatingNum);
        System.out.println(doubleNumber);
        System.out.println(isTrue);;
        System.out.println(letter);
        System.out.println(name);
    }
}
